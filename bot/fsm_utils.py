# -*- coding: utf-8 -*-
"""
Защита от "залипания" в FSM-состоянии.

Если бот ждёт от пользователя текст (название клана, девиз, сумму для банка
и т.п.), а пользователь вместо этого присылает команду (/shop, /iam и т.д.) —
без этой защиты команда была бы съедена FSM-хендлером как обычный текст, и
пользователь "терял" доступ ко ВСЕМ командам, пока не завершит/не отменит
текущий ввод. Эта функция ловит такие случаи, сбрасывает состояние и
подсказывает набрать команду ещё раз.
"""

from aiogram.fsm.context import FSMContext
from aiogram.types import Message


async def check_command_escape(message: Message, state: FSMContext) -> bool:
    """Возвращает True, если сообщение оказалось командой (не ожидаемым
    текстом) — в этом случае состояние уже сброшено и обработку нужно
    прекратить (return в вызывающем хендлере)."""
    text = (message.text or "").strip()
    if text.startswith("/"):
        await state.clear()
        cmd = text.split()[0]
        await message.reply(f"Отменено — вы ввели команду. Наберите {cmd} ещё раз.")
        return True
    return False
